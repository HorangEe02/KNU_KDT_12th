API_CALL_DELAY = 0.1  # API 호출 간 딜레이 (초)


def random_delay(min_sec=None, max_sec=None):
    """랜덤 딜레이 적용"""
    min_sec = min_sec or CRAWL_DELAY_MIN
    max_sec = max_sec or CRAWL_DELAY_MAX
    delay = random.uniform(min_sec, max_sec)
    time.sleep(delay)
    return delay


